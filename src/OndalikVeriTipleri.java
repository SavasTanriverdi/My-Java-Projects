
public class OndalikVeriTipleri {

	public static void main(String[] args) {
		
		
		// Float Variable
		float floatNumber1 = 34.5f;
		System.out.println(floatNumber1);
		
		float floatNumber2 = 34.1F;
		System.out.println(floatNumber2);
		
		float floatNumber3 = 34;
		System.out.println(floatNumber3);
		
		// Double Variable
		double doubleNumber1 = 3;
		System.out.println(doubleNumber1);
		
		double doubleNumber2 = 3.14;
		System.out.println(doubleNumber2);
		
		doubleNumber2 = 54.0;
		System.out.println(doubleNumber2);
		
	}
}
